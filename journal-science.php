                                             <div class="wpb_wrapper">
                                        <p>
                                            Submissions must follow the guidelines outlined below diligently. Manuscripts not conforming to these guidelines will be returned to the author(s). This can lead to delays in the publication of submissions.
                                        </p>
                                        
                                    <p>
                                       <b>General considerations</b>
                                       
   <ol type="1">
  <li><b>Originality of manuscripts.</b> Only Original submissions will be considered for publication. </li>
  <li><b>Simultaneous Submissions.</b> No manuscript may be submitted to ASUU Journal of Science (AJS) and any other journal at the same time. </li>
  <li><b>Revised manuscripts.</b> AJS does not publish manuscripts that are “revised” versions of manuscripts published elsewhere. </li>
  <li><b>Article Processing Fee.</b> AJS welcomes submissions all year-round and does not charge ARTICLE PROCESSING FEE NOR PAGE CHARGES.</li>
  <li><b>Scope.</b> AJS accepts submissions in the field of pure & applied Sciences such as nutrition & dietetics, fisheries & aquaculture, animal science, agronomy, medicine, food science & technology, microbiology, zoology, botany, biochemistry, science education, mathematics, statistics, veterinary medicine, nursing science, environmental science, pharmacy, computer science, software engineering, information systems, data science & engineering, cybersecurity and so on. </li>
</ol>
                                    </p>
                                   <p><b>Three types of Submissions to AJS: </b></p> 
                                     <p>
                                         
                                                   <b>Regular articles:</b>  These should describe new findings, and experimental procedures should be given in sufficient detail for others to verify the work. The length of a full paper should be the minimum required to describe and interpret the work clearly. The manuscript for an article shall normally be between 10 and 15 pages (inclusive of tables and figures).
                                    </p>
                                    <p>
                               <b> Short communications:</b> A Short communication is suitable for recording the results of completed small investigations or giving details new models, innovative methods, techniques or apparatus. The style of main sections need not conform to that of full-length papers. Short communications should not exceed 8 pages in length.
                                </p>
                                <p><b> Reviews:  </b> Submission of reviews and perspectives covering topics of current interest are welcomewhen invited.</p>
                                
                                     <p><b>Presentation of the Articles</b><br />

All portions of the manuscript must be typed double-spaced, in Microsoft Word (Times New Roman), and all pages numbered starting from the title page.
<p><b>Title </b> <br />The Title should be a brief phrase describing the contents of the paper. The Title Page should include the authors' full names and affiliations as well as the name of the corresponding author along with phone, fax and e-mail information.</p>


<b>Abstract</b> <br />The Abstract Should not exceed 250 words. There should be brief presentation of the topic. The scope of the experiments should be stated, and significant data indicated. All the major findings and conclusions should be pointed out. No literature should be cited within.

Following the abstract, about 3 to 5 <b>keywords</b> that will provide indexing references should be listed.
</p>

<p><b>Abbreviations</b> <br /> A list of non-standard Abbreviations should be added. In general, non-standard abbreviations should be used only when the full term is very long and used often. Each abbreviation should be spelt out and introduced in parentheses the first time it is used in the text. Only recommended SI units should be used. Standard abbreviations (such as ATP and DNA) need not be defined.</p>
  
  <p><b>Introduction</b><br />
      
      The Introduction should provide a clear statement of the problem, the relevant literature on the subject, and the proposed approach or solution. It should be understandable to colleagues from a broad range of scientific disciplines.
  </p>
  
 <p><b>Methodology</b><br />
 
 Methodology should be complete enough to allow experiments to be reproduced. However, only truly new procedures should be described in detail; previously published procedures should be cited, and important modifications of published procedures should be mentioned briefly. Capitalize trade names and include the manufacturer's name and address. Subheadings should be used. Methods in general use need not be described in detail.
 </p>
  <p><b>Results</b><br />
 
 Results should be presented with clarity and precision. The results should be written in the past tense when describing findings in the authors' experiments. Previously published findings should be written in the present tense.
 </p>
   <p><b>Discussion</b><br />
 
 The Discussion should interpret the findings in view of the results obtained in this and in past studies on this topic. State the conclusions in a few sentences at the end of the paper. 
 </p>
 
  <p><b>Tables</b><br />
 
  Tables should be kept to a minimum and be designed to be as simple as possible. Tables are to be typed double-spaced throughout, including headings and footnotes. Each table should be on a separate page, numbered consecutively in Arabic numerals and supplied with a heading and a legend. Tables should be self-explanatory without reference to the text. The details of the methods used in the experiments should preferably be described in the legend instead of in the text. The same data should not be presented in both table and graph form or repeated in the text.
 </p>
  <p><b>Figure legends</b><br />
 
 Figure legends should be typed in numerical order on a separate sheet. Graphics should be prepared using applications capable of generating high resolution GIF, TIFF, JPEG or PowerPoint. Begin each legend with a title and include sufficient description so that the figure is understandable without reading the text of the manuscript. Information given in legends should not be repeated in the text.
 </p>
 
 <p><b>Equations</b><br />
   Authors should use equation editor in MS-Word. <br />
   Equations should be numbered consecutively.

 </p>
  <p><b>References</b><br />
 References should follow the APA style. In the text, a reference identified by means of an author's name should be followed by the date of the reference in parentheses. When there are more than two authors, only the first author's name should be mentioned, followed by 'et al'. </p>
      
                                                      
                                             
                                                

